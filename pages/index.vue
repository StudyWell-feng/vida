<script setup lang="ts">
import { onMounted, onBeforeUnmount, ref, } from "vue";
import Swiper from "../components/swiper.vue";
import Splide from "@splidejs/splide";
import { voiceList, gameInfo } from "../utils/json";

const nuxtApp = useNuxtApp()


const show = ref(true)
let remValue = 0;
let isPhone = false;


function scrollHandler() {
  const nav = document.querySelector("nav");
  const logo = document.querySelector("nav .logo");

  if (window.scrollY > 10) {
    nav?.classList.add("opaque");
    logo?.setAttribute("src", "./Logo/logo.png");
  } else {
    nav?.classList.remove("opaque");
    logo?.setAttribute("src", "./Logo/logo.png");
  }
}
function checkScroll() {
  var fadeInElements = document.querySelectorAll(".fade-base");
  var windowH = window.innerHeight;
  fadeInElements.forEach(function (ele) {
    var rect = ele.getBoundingClientRect();
    if (rect.top < windowH && rect.bottom > 0) {
      ele.classList.add("fade-in");
    }
  });
}
function setRem() {
  isPhone = false;
  if (document.body.clientWidth >= 1200) {
    remValue = 16;
    document.documentElement.style.fontSize = remValue + "px";
  } else if (document.body.clientWidth >= 768) {
    remValue = 16;
    document.documentElement.style.fontSize = remValue + "px";
  } else {
    isPhone = true;
    var ratio = 50;
    const viewWidth = window.innerWidth;
    remValue = viewWidth / ratio;
    document.documentElement.style.fontSize = remValue + "px";
  }
}
function navInit() {
  document.querySelectorAll(".navbar-collapse a").forEach(function (navLink) {
    navLink.addEventListener("click", function (event) {
      // event.preventDefault()
      if (isPhone) {
        let bsCollapse = new bootstrap.Collapse(
          document.querySelector(".navbar-collapse"),
          { toggle: false }
        );
        bsCollapse.toggle();
      }
      let targetId = event?.currentTarget?.getAttribute("to");
      scrollTo(targetId);
    });
  });
}
function scrollTo(queryStr) {
  const element = document.querySelector(queryStr);
  // const isPhone = window.innerWidth <= 768; // 根据屏幕宽度判断是否是手机

  if (element) {
    const offset = element.offsetTop - (isPhone ? 64 : 88);

    window.scrollTo({
      top: offset,
      behavior: "smooth", // 如果想要平滑滚动，可以用 'smooth' 代替 'auto'
    });
  }
}
function initSplide() {
  new Splide("#splide", {
    type: "fade",
    autoWidth: true,
    autoheight: true,
    pagination: false,
    arrows: true,
    autoplay: true,
    rewind: true,
    cloneStatus: true,
    pauseOnHover: false,
    pauseOnFocus: false,
  }).mount();
}
function init() {
  setRem();
  scrollHandler();
}
const onScroll = () => {
  scrollHandler();
  checkScroll();
};
const os = ref('iOS');

function getOS() {
  const userAgent = navigator.userAgent || navigator.vendor || window.opera;
  // 检测 iOS
  if (/iPad|iPhone|iPod|Mac OS/.test(userAgent) && !window.MSStream) {
    return "iOS";
  }
  return "others";
}
onMounted(() => {
  const handleLoadingStart = () => (show.value = true)
  const handleLoadingFinish = () => (show.value = false)

  // const nuxtApp = useNuxtApp()
  nuxtApp.hook('page:loading:start', handleLoadingStart)
  nuxtApp.hook('page:loading:finish', handleLoadingFinish)

  os.value = getOS();
  init();
  navInit();
  checkScroll();
  initSplide();
  window.addEventListener("resize", init);
  window.addEventListener("scroll", onScroll);
});
onBeforeUnmount(() => {
  nuxtApp.hook('page:loading:start', null)
  nuxtApp.hook('page:loading:finish', null)
  window.removeEventListener("scroll", onScroll);
  window.removeEventListener("resize", init);
});
</script>

